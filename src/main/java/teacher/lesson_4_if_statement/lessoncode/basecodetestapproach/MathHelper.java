package teacher.lesson_4_if_statement.lessoncode.basecodetestapproach;

public class MathHelper {

    public boolean ifNumberIsEven(int number) {
        if (number % 2 == 0) {
            return true;
        }
        return false;
    }

    public int sum(int xValue, int yValue) {
        return xValue + yValue;
    }

}
