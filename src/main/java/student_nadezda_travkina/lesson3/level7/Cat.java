package student_nadezda_travkina.lesson3.level7;

public class Cat {
    String name;
    int age;
    String breed;

    Cat(String name, int age, String breed) {
        this.name = name;
        this.age = age;
        this.breed = breed;
    }

    String getName() {
        return name;
    }

    int getAge() {
        return age;
    }

    String getBreed() {
        return breed;
    }
}

