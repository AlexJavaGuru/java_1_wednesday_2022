package student_alex_stranger.lesson_4.level_7;

class Calculator {

    public int sum(int a, int b) {
        return a + b;
    }
    public int sub(int a, int b) {
        return a - b;
    }
    public int mul(int a, int b) {
        return a * b;
    }
    public int div(int a, int b) {
        return a / b;
    }
    public boolean isEven(int number) {
        return number % 2 == 0;
    }

}
