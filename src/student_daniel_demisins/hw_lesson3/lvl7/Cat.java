package student_daniel_demisins.hw_lesson3.lvl7;

public class Cat {
    String name;
    int age;
    String color;

    Cat (String name, int age, String color){
        this.name= name;
        this.age= age;
        this.color= color;
    }
    String getName(){
        return this.name;

    }
    int getAge(){
        return this.age;
    }

    String getColor(){
        return this.color;
    }

}
