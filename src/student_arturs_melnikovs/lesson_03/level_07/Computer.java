package student_arturs_melnikovs.lesson_03.level_07;

class Computer {
    String manufacturer;
    String model;

    Computer(String manufacturer, String model) {
        this.manufacturer = manufacturer;
        this.model = model;
    }

    String getManufacturer() {
        return this.manufacturer;
    }
    String getModel() {
        return this.model;
    }
}
